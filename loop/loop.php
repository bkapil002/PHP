<!-- FOR -->

<?php
 for($i = 0; $i <= 5 ; $i++){
    echo "The number is: $i <br>";
 }
 ?>

 <!-- while -->


  <?php
$i = 1;
 while($i <= 10){
    echo "The number is: $i <br>";
    $i++;
 }
 ?>

 <!-- do while -->
  
<?php
$i = 1;
do{
    echo "The number is: $i <br>";
    $i++;
}while($i <= 5)
?>